# 3.b) Python program using while loop to Find sum of first 'n' natural numbers

'''
i=1
sum=0
n= int(input("Enter the limit : "))
while (i<=n):
    sum += i
    i+=1
print("Sum = ", sum)
'''

# 3.c) Python program using while loop to Generate multiplication table (till 10) of any given number

'''
i=1
n= int(input("Enter the limit : "))
while (i<=n):
    j=1
    while (j<=10):
        print(i,"x",j,"=",i*j)
        j+=1
    print()
    i+=1
'''
